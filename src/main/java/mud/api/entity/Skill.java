package mud.api.entity;

public class Skill {
    private int id;
    private String name;
    private String doingMessage;
    private String damage;
    private String type;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getDoingMessage() {
        return doingMessage;
    }

    public String getDamage() {
        return damage;
    }

    public String getType() {
        return type;
    }
}
